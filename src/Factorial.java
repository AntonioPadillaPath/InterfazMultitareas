    private void numeroKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_numeroKeyTyped
        
        char numero = evt.getKeyChar();
        
        if(Character.isLetter(numero))
        {
            getToolkit().beep();
            evt.consume();
            resultado.setEnabled(false);
        }
        
        else
        {
            resultado.setEnabled(true);
        }
        
    }//GEN-LAST:event_numeroKeyTyped

    
    private void resultadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resultadoActionPerformed
        try{
            
        int i;
        double n;
        double factorial = 1;
        
        n = Double.parseDouble(this.numero.getText());
        
        if(n < 0)
        {
            texto.setText("No hay factorial en nÃºmeros negativos");
            Factorial.setText("");
        }
        
        else if(n == 0 || n == 1)
        {
            texto.setText("El factorial de " +n+ " es:");
            Factorial.setText("1");
        }
        
        else
        {
            texto.setText("El factorial de " +n+ " es:");
            
            for(i = 1; i <= n; i++)
            {
                factorial *= i;
            }
            
            Factorial.setText(String.valueOf(factorial));
            
        }}catch (Exception e) {JOptionPane.showMessageDialog(null, "Introduzca el tipo de dato solicitado", "Error", JOptionPane.ERROR_MESSAGE);}
      
    }//GEN-LAST:event_resultadoActionPerformed

